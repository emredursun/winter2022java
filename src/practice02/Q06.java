public class Q06 {
    public static void main(String[] args) {

            /*
             Print on the console numbers from 1 to 100.
             But do not use any number in your code!
             */
        for (int i='a'/'a'; i<='d'; i++){
            System.out.print(i + " ");
        }


        // 2. Way

        int n = 'a'/'a'; // 'b' - 'a'
        int m = 'd'; // ASCII value of d is ==> 100
        while (n <= m){
            System.out.print(n + " ");
            n++;
        }


    }
}
